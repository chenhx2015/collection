// 使用扩展运算符，和数组相关的操作
const a1 = [1, 2];
const a2 = a1;
a2[0] = 2;
console.log(a1); // [2, 2] 说明原数组会被修改

// ES5 要用变通的方法来赋值数组
const b1 = [1, 2];
const b2 = a1.concat();

b2[0] = 2;
console.log(b1); // [1, 2] 说明使用这种方法不会修改原数组

// 看看扩展运算符会不会改变原数组
const c1 = [1, 2];
const c2 = [...c1];
c2[0] = 2;
console.log(c1); // [1, 2] 说明数组使用扩展运算符不会改变原数组
